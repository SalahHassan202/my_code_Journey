void main()
{
  // var keyword

  var name = 'salah' ;    // name ===> string
  print(name) ; 

  var age = 10 ;                // age ===> int 
  print (age) ;

  var rate = 4.2 ;          // rate ===> double 
  print(rate) ;

  var text ;         // text ===> dynamic 
  // text ===> string , num , bool , int . double , ....

  text = 10 ;  // int 
  text = 20.3 ;  // double
  text = 'saber' ; // string
  text = true  ;   // bool   
  print(text) ; 
}